// For Loop
var num;
for (num=0; num<=10; num++) {
    if(num==5){
        break;
    }
    console.log("Number in For: "+ num);
}

//Loop While
var idx=0;
while(idx<=5){
    console.log("Number in While: "+idx);
    idx++;
}

